function mask_sr(image_sr) {
    var mask1 = ee.Image(image_sr).select('pixel_qa').lt(325);
    var mask2=(ee.Image(image_sr).select('B2').gt(4800).and(ee.Image(image_sr).select('B3').gt(4800)).and(ee.Image(image_sr).select('B4').gt(4800))).not();
   return image_sr.mask(mask1).updateMask(mask2);
}

